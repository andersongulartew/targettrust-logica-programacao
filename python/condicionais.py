## Condicionais

# if, elif, else ============================================

valor01 = 9
valor02 = 19

#diferença entre AND e OR
# AND
if (valor01 > 10) and (valor02 < 20):
  print("estou aqui")

else:
  print("não entrou aqui")

# OR
if (valor01 > 10) or (valor02 < 20):
  print("estou aqui")

else:
  print("não entrou aqui")

#será verdeiro se os dois forem fazer sentido igual no OR
#será falso se os dois forem inverso igual no AND
